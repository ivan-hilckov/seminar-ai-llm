/**
 * Разбиение по частям. 'opening' (62) | 'IV' (63–69) | 'V' (70–87) | 'VI' (88–102) | 'closing' (103–104).
 */
export function partOf(slide) {
  const n = Number(slide.id);
  if (n === 62) return 'opening';
  if (n <= 69) return 'IV';
  if (n <= 87) return 'V';
  if (n <= 102) return 'VI';
  return 'closing';
}

